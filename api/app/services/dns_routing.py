PRODUCTION_UPSTREAM_TEMPLATE = dedent("""\
    # Auto-generated by TBD Platform — Persistent Production URL
    # Project: {project_slug}
    # Hostname: {hostname}
    # Backed by deploy: {deploy_id}
    # Generated: {timestamp}

    upstream {upstream_name} {{
        server {backend_ip}:{backend_port};
    }}
""")

PRODUCTION_SERVER_TEMPLATE = dedent("""\
    # {hostname} -> {backend_ip}:{backend_port} (production alias)
    server {{
        listen 443 ssl;
        server_name {hostname};

        ssl_certificate     /etc/nginx/ssl/tbd.crt;
        ssl_certificate_key /etc/nginx/ssl/tbd.key;

        location / {{
            proxy_pass http://{upstream_name};
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto $scheme;
            proxy_http_version 1.1;
            proxy_set_header Upgrade $http_upgrade;
            proxy_set_header Connection "upgrade";
        }}

        location /health {{
            proxy_pass http://{upstream_name}/health;
        }}
    }}
""")


def _make_production_upstream_name(project_slug: str) -> str:
    """Generate a safe Nginx upstream name for a production alias.

    Example: 'my-app' -> 'prod_my_app'
    """
    safe = project_slug.replace("-", "_")[:32]
    return f"prod_{safe}"


async def register_production_routing(
    project_slug: str,
    owner_username: str,
    backend_ip: str,
    backend_port: int,
    deploy_id: str = "",
) -> Path:
    """Write/overwrite the persistent production URL Nginx config.

    This is called every time a new deploy is promoted to active.
    It overwrites the previous config so the production hostname
    seamlessly switches to the new backend.

    Acquires a per-project lock so that two concurrent deploys for
    the same project cannot race — only one writes the config file
    at a time.

    Config file: {nginx_upstream_dir}/prod-{slug}.conf

    Returns:
        Path to the written config file.
    """
    from datetime import datetime, timezone

    lock = _get_production_lock(project_slug)
    async with lock:
        UPSTREAM_DIR.mkdir(parents=True, exist_ok=True)

        backend_ip = _strip_cidr(backend_ip)
        upstream_name = _make_production_upstream_name(project_slug)
        hostname = production_hostname(project_slug, owner_username)
        timestamp = datetime.now(timezone.utc).isoformat()

        config = PRODUCTION_UPSTREAM_TEMPLATE.format(
            project_slug=project_slug,
            hostname=hostname,
            deploy_id=deploy_id,
            timestamp=timestamp,
            upstream_name=upstream_name,
            backend_ip=backend_ip,
            backend_port=backend_port,
        )

        config += "\n" + PRODUCTION_SERVER_TEMPLATE.format(
            hostname=hostname,
            upstream_name=upstream_name,
            backend_ip=backend_ip,
            backend_port=backend_port,
        )

        filename = f"prod-{project_slug}.conf"
        config_path = UPSTREAM_DIR / filename
        config_path.write_text(config)

        logger.info(
            "Wrote production Nginx config: %s (%s -> %s:%d)",
            config_path, hostname, backend_ip, backend_port,
        )

        await signal_nginx_reload()
        return config_path


async def unregister_production_routing(project_slug: str) -> bool:
    """Remove the persistent production URL Nginx config.

    Called when a project is fully torn down (no active deploys remain).
    """
    filename = f"prod-{project_slug}.conf"
    config_path = UPSTREAM_DIR / filename

    if config_path.exists():
        config_path.unlink()
        logger.info("Removed production Nginx config: %s", config_path)
        try:
            await signal_nginx_reload()
        except DNSRoutingError:
            logger.error("Failed to signal Nginx reload after removing production config for %s", project_slug)
        return True

    return False
